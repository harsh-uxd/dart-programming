void main() {
  // loops

  // for loop - definite (entry control)
  for (var i = 0; i < 5; i++) {
    print("Hello!");
  }

  // while loop - indefinite (entry control)
  var j = 0;
  while (j < 5) {
    print("Hello!");
    j++;
  }

  // do while loop - indefinite (exit control)
  var k = 0;
  do {
    print("Hello!");
    k++;
  } while (k < 5);
}
